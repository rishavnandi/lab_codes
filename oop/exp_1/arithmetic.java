package exp_1;
// WAP to perform basic arithmetic operation on two variables

public class arithmetic {
    public static void main(String args[]) {
        int a = 10, b = 5;
        // int b = 5;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
